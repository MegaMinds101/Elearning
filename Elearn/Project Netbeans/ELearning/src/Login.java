int getMM(String crsInApp){
    int jmm1 = 0,jmm2 = 0,jmm3 = 0,jmm4 = 0,tjmm=0;
    try{
        sConnect();
    
        BasicDBObject match = new BasicDBObject();
        match.put( "uname", username);
        DBCursor cursor  = student.find( match );
        BasicDBObject account = null;
        while( cursor.hasNext() )
            account = ( BasicDBObject ) cursor.next();
        //System.out.println( ( String ) account.get( "uname" ) + ":" );
        BasicDBList javaEM = ( BasicDBList ) account.get( crsInApp );
        for( Iterator< Object > it = javaEM.iterator(); it.hasNext(); )
        {
            BasicDBObject dbo     = ( BasicDBObject ) it.next();
            BasicDBObject b = ( BasicDBObject ) dbo;
            jmm1=(int) b.get("MM1");
            jmm2=(int) b.get("MM2");
            jmm3=(int) b.get("MM3");
            jmm4=(int) b.get("MM4");
            //System.out.println(  b.get("MM1")+ b.get("MM2") );
        }
        tjmm=jmm1+jmm2+jmm3+jmm4;
        return tjmm;
    }
    catch(Exception e){
        System.out.print(e.getMessage());
    }
    return tjmm;
}

